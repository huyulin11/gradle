package com.ytgrading.bean.wms;

public class DataFromWMS {
	private String stMessageFrom;
	private String stSubject;
	private String stSignKey;
	private String stRequest;

	public String getStMessageFrom() {
		return stMessageFrom;
	}

	public void setStMessageFrom(String stMessageFrom) {
		this.stMessageFrom = stMessageFrom;
	}

	public String getStSubject() {
		return stSubject;
	}

	public void setStSubject(String stSubject) {
		this.stSubject = stSubject;
	}

	public String getStSignKey() {
		return stSignKey;
	}

	public void setStSignKey(String stSignKey) {
		this.stSignKey = stSignKey;
	}

	public String getStRequest() {
		return stRequest;
	}

	public void setStRequest(String stRequest) {
		this.stRequest = stRequest;
	}

}